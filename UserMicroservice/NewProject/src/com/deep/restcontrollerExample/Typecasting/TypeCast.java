package com.deep.restcontrollerExample.Typecasting;

public class TypeCast {
    public static void main(String[] args) {

       //Implicit
        int a =34;
        long b = a;
        System.out.println(b);
        //Explicit
        long c =45;
        int d =(int) c;
        System.out.println(d);




    }
}
